package OOP.practice;

public class Wrapper {
    public static void main(String[] args) {
        // wrapper calsses = provides a way to use primitive data types as reference
        // data types
        // reference data types contain useful methods e.g. String
        // can be used with collection(ex.ArrayList)

        // primitve wrapper
        // -------- -------
        // boolean Boolean
        // char Character
        // int Integer
        // double Double

        // autoboxing = the automatic conversion that the java compiler makes between
        // the primitive type and refrence type
        // unboxing = the reverse of auto boxing

        Boolean a = true;
        Character b = '@';
        Integer c = 13;
        Double d = 3.22;
        String e = "hello";

    }
}
